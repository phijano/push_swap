#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	if ((unsigned char *)dst > (unsigned char *)src)
	{
		while (len > 0)
		{
			((unsigned char *)dst)[len - 1] = ((unsigned char *)src)[len - 1];
			len--;
		}
	}
	else if ((unsigned char *)dst < (unsigned char *)src)
		ft_memcpy(dst, src, len);
	return (dst);
}
